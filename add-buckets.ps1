# Script to add aditional "repositories of additional programs" ... aka called a "bucket"

# add "extras" bucket
scoop bucket add extras
# add versions bucket for convience.
scoop bucket add versions 

# add "awesome-scoop" github auto-scraped scoop bucket repositories. (prolly not the safest thing in a security standpoint, but its EZ)
scoop bucket add awesome-scoop https://github.com/tapanchandra/awesome-scoop

